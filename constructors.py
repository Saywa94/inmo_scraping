class Propiedad:
    def __init__(
        self,
        titulo,
        precio,
        ciudad,
        zona,
        tipo_oferta,
        tipo_bien,
        superficie,
        n_habitaciones,
        n_wc,
        ):
        self.titulo = titulo
        self.precio = precio
        self.ciudad = ciudad
        self.zona = zona
        self.tipo_oferta = tipo_oferta
        self.tipo_bien = tipo_bien
        self.superficie = superficie
        self.n_habitaciones = n_habitaciones
        self.n_wc = n_wc
